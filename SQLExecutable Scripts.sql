---------------------CREATING TABLES-------------------------
CREATE TABLE MEMBER(
	MEMBER_ID				INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	SSN						VARCHAR(9),
	FIRST_NAME				VARCHAR(50),
	LAST_NAME				VARCHAR(50),
	BIRTH_DATE				DATE,
	GENDER					CHAR(1),
	MARITAL_STATUS			VARCHAR(10),
	ETHNICITY				VARCHAR(30),
	CARD_ID					INT,
	GROUP_ID				INT,
	BILLING_ID				INT,
	)

CREATE TABLE HEALTHCARD(
	CARD_ID						INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	ID_CARD_ISSUE_DATE			DATE,
	CARD_STATUS					VARCHAR(20),
	ID_CARD_TERMINATION_DATE	DATE
)

CREATE TABLE POLICY(
	POLICY_ID				INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	POLICY_START_DATE		DATE,
	POLICY_END_DATE			DATE,
	GROUP_ID				INT,
	COVERAGE_ID				INT
)
 
CREATE TABLE MEMBER_POLICY(
	POLICY_ID		INT NOT NULL,
	MEMBER_ID		INT NOT NULL 
)

