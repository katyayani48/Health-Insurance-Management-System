--UPDATE QUERY
UPDATE BILLING 
SET BILL_AMOUNT =   TEMP.BIL_AMOUNT
FROM (
SELECT BILL.BILLING_ID,(COV.AMOUNT/12) BIL_AMOUNT 
	FROM MEMBER M,
		 COVERAGE COV,
		 BILLING BILL,
	     POLICY P,
		 MEMBER_POLICY MP
	WHERE BILL.BILLING_ID = M.BILLING_ID
	AND MP.POLICY_ID = P.POLICY_ID
	AND P.COVERAGE_ID = COV.COVERAGE_ID
) TEMP
 WHERE BILLING.BILLING_ID = TEMP.BILLING_ID

