--JOIN QUERY
--QUERY 1, SHOW ALL MEMBERS WHO PAYING BILL BY CASH 

SELECT M.MEMBER_ID,
	   M.FIRST_NAME+' '+M.LAST_NAME MEMBER_NAME,
	   B.BILLING_ID,
	   PM.PAYMENT_TYPE
FROM BILLING B
JOIN MEMBER M
	ON B.BILLING_ID = M.BILLING_ID
JOIN PAYMENT_MODE PM
	ON PM.PAYMENT_MODE_ID = B.PAYMENT_MODE_ID
WHERE PM.PAYMENT_TYPE = 'CASH'

--QUERY 2, FIND OUT ACTIVE HEALTH CARD MEMBERS TO SEND HEALTH CARD IN EMAIL

SELECT H.CARD_ID,
       M.FIRST_NAME+' '+M.LAST_NAME MEMBER_NAME,
	   C.EMAIL,
	   H.CARD_STATUS
FROM HEALTHCARD H
JOIN MEMBER M
	ON H.CARD_ID=M.CARD_ID
JOIN CONTACT C
    ON C.MEMBER_ID= M.MEMBER_ID
	WHERE H.CARD_STATUS = 'IN PROGRESS'
	
--GROUPBY QUERY
--FIND OUT NUMBER OF MEMBERS WHO DID NOT PAY THE BILL YET---
SELECT COUNT(M.MEMBER_ID) TOTAL_MEMBER_NONPAID,
	   B.BILL_STATUS
FROM MEMBER M
JOIN BILLING B
	ON M.BILLING_ID= B.BILLING_ID 
WHERE B.BILL_STATUS='NOT PAID'
GROUP BY BILL_STATUS 

-- LIST OUT THE NUMBER OF MEMBERS OF EACH EHTNICITY
SELECT COUNT(M.ETHNICITY) COUNT_ETHNICITY,
	   M.ETHNICITY 
FROM MEMBER M
GROUP BY ETHNICITY
ORDER BY ETHNICITY DESC

--SUBQUERY
