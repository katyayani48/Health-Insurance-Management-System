CREATE TABLE COVERAGE(
	COVERAGE_ID			INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	COVERAGE_TYPE		VARCHAR(100),
	AMOUNT				MONEY,
) 

CREATE TABLE [GROUP](
	GROUP_ID					INT IDENTITY(1,1)NOT NULL PRIMARY KEY,
	GROUP_NAME					VARCHAR(50),
	GROUP_SIZE                  INT,
	GROUP_EFFECTIVE_DATE		DATE,
	GROUP_TERMINATON_DATE		DATE,
)

CREATE TABLE CONTACT(
	CONTACT_ID			INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	EMAIL				VARCHAR(50),
	PHONE_NUMBER		VARCHAR(12),
	MEMBER_ID			INT
)

CREATE TABLE ADDRESSINFO(
	ADDRESSINFO_ID		INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	ADDRESS_LINE_1		VARCHAR(100),
	ADDRESS_LINE_2		VARCHAR(100),
	CITY_NAME			VARCHAR(100),
	COUNTRY_NAME		VARCHAR(100),
	POSTAL_CODE			VARCHAR(10),
	MEMBER_ID			INT
)

CREATE TABLE BILLING(
	BILLING_ID			INT NOT NULL PRIMARY KEY,
	PAYMENT_MODE_ID		INT,
	BILL_AMOUNT			MONEY,
	PAID_DATE			DATE,
	DUE_DATE			DATE,
	BILL_DATE			DATE,
	BILL_STATUS			VARCHAR(20)
)

CREATE TABLE PAYMENT_MODE(
	PAYMENT_MODE_ID		INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	PAYMENT_TYPE		VARCHAR(100),
) 

CREATE TABLE CLAIM(
	CLAIM_ID			INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	CLAIM_TYPE_ID       INT,
	CLAIM_AMOUNT		MONEY,
	CLAIM_STATUS		VARCHAR(20),
	MEMBER_ID			INT
)

CREATE TABLE CLAIM_TYPE(
	CLAIM_TYPE_ID		INT IDENTITY(1,1) NOT NULL PRIMARY KEY,
	CLAIM_TYPE			VARCHAR(100)
)

